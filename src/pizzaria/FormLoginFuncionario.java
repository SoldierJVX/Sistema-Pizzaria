    private Funcionario buscarFuncionario(ArrayList<Funcionario> listaFuncionarios) {

        int i;

        for (i = 0; i < listaFuncionarios.size() && !listaFuncionarios.get(i).getUsuario().equals(txtUsuario.getText()) && listaFuncionarios.get(i).getSenha().equals(txtSenha.getText()); i++)
            ;

        if (i < listaFuncionarios.size()) {

            return listaFuncionarios.get(i);

        }

        return null;

    }

