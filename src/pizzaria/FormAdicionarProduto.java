    private void tblProdutosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMouseReleased

        if (tblProdutos.getSelectedRow() != -1) {

            btnAdicionar.setEnabled(true);

            String descricao = listaProdutos.get(tblProdutos.getSelectedRow()).getDescricao();

            txtDescricao.setText(descricao);

        } else {

            btnAdicionar.setEnabled(false);

        }


    }//GEN-LAST:event_tblProdutosMouseReleased

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        if (tblProdutos.getSelectedRow() != -1) {

            JTable listaProdutosPedido = frmPedido.getTblListaProdutos();
            JTextField campoTotal = frmPedido.getTxtTotal();
            JButton btnRemoverProduto = frmPedido.getBtnRemoverProduto();
            NumberFormat formatador = NumberFormat.getCurrencyInstance();
            double total = frmPedido.getTotal();

            Object[] dados = new Object[4];

            DefaultTableModel modelo = (DefaultTableModel) listaProdutosPedido.getModel();

            Produto produtoEscolhido = listaProdutos.get(tblProdutos.getSelectedRow());

            dados[0] = produtoEscolhido.getCodProduto() + 1;
            dados[1] = produtoEscolhido.getNome();
            dados[2] = formatador.format(produtoEscolhido.getPreco());
            dados[3] = Integer.parseInt(txtQuantidade.getText());

            modelo.addRow(dados);

            listaProdutosPedido.setModel(modelo);

            total += produtoEscolhido.getPreco() * Integer.parseInt(txtQuantidade.getText());
            
            frmPedido.setTotal(total);

            campoTotal.setText(formatador.format(total));

            btnRemoverProduto.setEnabled(true);

            btnCancelarActionPerformed(evt);

        }


    }//GEN-LAST:event_btnAdicionarActionPerformed

