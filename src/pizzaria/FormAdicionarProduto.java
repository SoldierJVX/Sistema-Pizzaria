    private void carregarProdutos() {

        Object[] dados = new Object[3];
        DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
        NumberFormat formatador = NumberFormat.getCurrencyInstance();
        ArrayList<Produto> listaProdutos = frmPedido.listaProdutos;

        for (int i = 0; i < frmPedido.listaProdutos.size(); i++) {

            dados[0] = (listaProdutos.get(i).getCodProduto() + 1) + "";
            dados[1] = listaProdutos.get(i).getNome();
            dados[2] = formatador.format(listaProdutos.get(i).getPreco());

            modelo.addRow(dados);

            tblProdutos.setModel(modelo);

        }
    }
