    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String nome, usuario, senha, confSenha;

        nome = txtNome.getText();
        usuario = txtUsuario.getText();
        
        senha = new String(txtSenha.getPassword());
        confSenha = new String(txtConfSenha.getPassword());

        if (senha.equals(confSenha)) {

            senha = new String(txtSenha.getPassword());

            listaFuncionarios.add(new Funcionario(usuario, senha, nome));

            JOptionPane.showMessageDialog(null, "Funcionario cadastrado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            
            frmMenu.setEnabled(true);
            this.dispose();

        } else {

            JOptionPane.showMessageDialog(null, "Senhas n√£o conferem!", "Erro", JOptionPane.INFORMATION_MESSAGE);

        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

